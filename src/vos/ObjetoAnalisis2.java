package vos;

public class ObjetoAnalisis2 {

	private int idProducto;
	
	private int local;

	public ObjetoAnalisis2(int idProducto, int local) {
		this.idProducto = idProducto;
		this.local = local;
	}

	public int getIdProducto() {
		return idProducto;
	}

	public void setIdProducto(int idProducto) {
		this.idProducto = idProducto;
	}

	public int getLocal() {
		return local;
	}

	public void setLocal(int local) {
		this.local = local;
	}
	
	
}
